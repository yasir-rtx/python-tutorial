# Operasi Aritmatika
x = 10
y = 4

# Penjumlahan
print(x, '+', y, '=', x + y)

# Pengurangan
print(x, '-', y, '=', x - y)

# Perkalian
print(x, '*', y, '=', x * y)

# Pembagian
print(x, '/', y, '=', x / y) #otomatis jadi float

# Eksponen
print(x, '**', y, '=', x ** y)

# Modulus
print(x, '%', y, '=', x % y)

# Floor Division
print(x, '//', y, '=', x // y) #hasil akan dibulatkan ke bawah

# Prioritas Operasi Aritmatika
# 1. () --> Paling OP, wkwkwk
# 2. **
# 3. *, /, //
# 4. +, -